export type TranslationKey = keyof typeof translations.he

export function t(lang: Lang, key: TranslationKey): string {
  return translations[lang][key] ?? translations.he[key] ?? key
}

export function formatCurrency(value: number, currency: string = 'ILS', lang: Lang = 'he'): string {
  const locale = lang === 'he' ? 'he-IL' : 'en-US'
  return new Intl.NumberFormat(locale, {
    style: 'currency',
    currency,
    maximumFractionDigits: 0,
  }).format(value)
}

export function formatPercent(value: number, lang: Lang = 'he'): string {
  const locale = lang === 'he' ? 'he-IL' : 'en-US'
  return new Intl.NumberFormat(locale, {
    style: 'percent',
    minimumFractionDigits: 1,
    maximumFractionDigits: 2,
  }).format(value / 100)
}
